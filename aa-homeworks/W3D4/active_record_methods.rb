.select(something)
Returns an array of all your objects, but just the "something" that you pass into the argument.

EXAMPLES:

    User.select([:id, :name])
        => [ <User id: 1, name: "Oscar">, <User id: 2, name: "Felix">, User id: 3, name: "Jonathan"> ]


